    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:

        String codigo = txtCodigo.getText();
        String nombre = txtNombre.getText();
        double precio = Double.parseDouble(txtPrecio.getText());
        int cantida = Integer.parseInt(txtCantida.getText());
        String categoria = txtCategoria.getText();
        String descripcion = txtDescripcion.getText();
        String cedula = ventanaUsuario.persona.getCedula();

        if (controlador.guardarProductString(codigo, nombre, precio, cantida, categoria, descripcion, cedula)) {
            JOptionPane.showMessageDialog(this, "Guardado exitosamente");
            limpiar();
            ventanaUsuario.setVisible(true);
            ventanaUsuario.listar();
            this.dispose();
            
        } else {
            JOptionPane.showMessageDialog(this, "Error al guardar");
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String codigo = txtCodigo.getText();

        if (!codigo.equals("")) {

            List<String> usuario = controlador.buscarProducto(codigo);
            if (!usuario.get(0).equals("")) {
                txtCodigo.setEnabled(false);
                cargarInformacion(usuario);
            } else {
                limpiar();
                JOptionPane.showMessageDialog(this, "No se encuentra");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Ingrese dato a buscar");
        }

    }//GEN-LAST:event_btnBuscarActionPerformed

