    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:

        String cedula = txtCodigo.getText();

        if (controlador.eliminarProducto(cedula)) {
            JOptionPane.showMessageDialog(this, "Eliminado exitosamente");
            ventanaUsuario.setVisible(true);
            this.dispose();
            ventanaUsuario.listar();
            limpiar();
          
        } else {
            JOptionPane.showMessageDialog(this, "Error al eliminar");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    
      public void limpiar() {
        txtNombre.setText("");
        txtPrecio.setText("");
        txtCodigo.setText("");
        txtCantida.setText("");
        txtCategoria.setText("");
        txtDescripcion.setText("");
        txtCodigo.setEnabled(true);
    }
      
      
       public void cargarInformacion(List<String> temp) {
        txtCodigo.setText(temp.get(0));
        txtNombre.setText(temp.get(1));
        txtPrecio.setText(temp.get(2));
        txtCantida.setText(temp.get(3));
        txtCategoria.setText(temp.get(4));
        txtDescripcion.setText(temp.get(5));
       }

