    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:

        String cedula = txtCedula.getText();
        String nombre = txtNombre.getText();
        String apellido = txtApellidos.getText();
        String correo = txtCorreo.getText();
        String contrasena = txtContrasena.getText();

        if (gestionUsuario.modificar(cedula, nombre, apellido, correo, contrasena)) {
            JOptionPane.showMessageDialog(this, "Modificado exitosamente");
            ventanaAdmin.setVisible(true);
            this.dispose();
            ventanaAdmin.listar();
            limpiar();
//            listar(); 
        } else {
            JOptionPane.showMessageDialog(this, "No se modifico ");
        }

    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String cedula = txtCedula.getText();

        if (!cedula.equals("")) {

            List<String> usuario = gestionUsuario.buscar(cedula);
            if (!usuario.get(0).equals("")) {
                txtCedula.setEnabled(false);
                cargarInformacion(usuario);
            } else {
                limpiar();
                JOptionPane.showMessageDialog(this, "No se encuentra");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Ingrese dato a buscar");
        }


    }//GEN-LAST:event_btnBuscarActionPerformed

