    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:

        String cedula = txtCedula.getText();

        if (gestionUsuario.eliminar(cedula)) {
            JOptionPane.showMessageDialog(this, "Eliminado exitosamente");
            ventanaAdmin.setVisible(true);
            this.dispose();
            ventanaAdmin.listar();
            limpiar();
//            listar();
        } else {
            JOptionPane.showMessageDialog(this, "Error al eliminar");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    public void limpiar() {
        txtNombre.setText("");
        txtApellidos.setText("");
        txtCedula.setText("");
        txtCorreo.setText("");
        txtContrasena.setText("");
        txtCedula.setEnabled(true);
    }

    public void cargarInformacion(List<String> temp) {
        txtCedula.setText(temp.get(0));
        txtNombre.setText(temp.get(1));
        txtApellidos.setText(temp.get(2));
        txtCorreo.setText(temp.get(3));
        txtContrasena.setText(temp.get(4));

    }

